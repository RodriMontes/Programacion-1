

# Suma - Devuelve la suma de dos numeros
print(9 + 5)

# Resta - Devuelve la resta de dos numeros
print(9 - 5)

# Multiplicacion - Devuelve el producto de dos numeros
print(9 * 5)

# Division - Devuelve el cociente de dos numeros
print(9 / 5)

# Division enterea / Devuelve el cociente entero de dos numeros
print(9 // 5)

# Potencia / Devuelve la potencia entre dos numeros
print(9 ** 5)

# Modulo o Residuo / Devuelve el resto del cociente entre numero
print(10 % 9)